package com.tnsif.day2;

public class DataTypes 
{

	public static void main(String[] args) 
	{
		//Integer 
		int num1 = 11/2;
		System.out.println("Num 1 (Int): " + num1);
		
		//Float
		float num2 = 101f/ 61f;
		System.out.println("Num 2 (Float): " + num2);
		
		//Double
		double num3 = 10d/6d;
		System.out.println("Num 3 (Double): " + num3);
		
		//Area of Circle
		int radius = 7;
		float pi = 3.14f;
		double area = 0; 
		area = radius * radius * pi;
		System.out.println("Area of Circle : " + area);
		
		
	}

}
