package com.tnsif.day2;

public class IFDemo 
{

	public static void main(String[] args) 
	{
		
		int x = 4;
		
		if(x == 4)
		{
			System.out.println("X == 4"); // It will print as the condition is satisfied
		}
		
		int x2 = 3;
		
		if(x2 == 4)
		{
			System.out.println("X2 != 4"); // It will not print as the condition is not satisfied
		}
			
			
	}

}
