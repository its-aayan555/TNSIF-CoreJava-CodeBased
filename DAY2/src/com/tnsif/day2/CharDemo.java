package com.tnsif.day2;

public class CharDemo 
{

	public static void main(String[] args) 
	{
		
		//Characters
		char chr = 'a';
		System.out.println("Char Data : " + chr);
		
		char chr2 = '1';
		System.out.println("Char Data : " + chr2);
		
		//Character using Integer
		char chr3 = 65;
		System.out.println("Char Data : " + chr3);
		
		//Integer using Character
		int var = 'A';
		System.out.println("Int Data : " + var);
		
		// Assigning unicode to char
		char var1='\u20AC';
		System.out.println(var1);

	}

}
